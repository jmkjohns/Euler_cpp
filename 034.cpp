#include <iostream>

//functions
int fact( int num );
int sumDigits( int num );

//=============================================================================
int main( int argc, char *argv[] )
//=============================================================================
{
	int total = 0;
	for (int i = 3; i < 10000000; ++i)
	{
		if (sumDigits( i ) == i)
		{
			total += i;
		}
	}
	std::cout << total << std::endl;
}


//=============================================================================
int fact( int num )
//=============================================================================
{
	if (num > 0)
	{
		return num * fact( num - 1 );
	}
	else
	{
		return 1;
	}
}


//=============================================================================
int sumDigits( int num )
//=============================================================================
{
	int result = 0;
	while (num > 0)
	{
		result += fact( num % 10 );
		num /= 10;
	}
	return result;
}
